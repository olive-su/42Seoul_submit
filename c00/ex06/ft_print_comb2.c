#include <unistd.h>

void	comb_sec_up(char *b, int *c)
{
	b[1]++;
	if (b[1] > '9')
	{
		b[0]++;
		b[1] = '0';
		if (b[0] > '9')
		{
			b[0] = '0';
			(*c)++;
		}
	}
}

void	comb_fir_up(char *a, int *c)
{
	if (*c)
	{
		a[1]++;
		if (a[1] > '9')
		{
			a[0]++;
			a[1] = '0';
		}
		(*c)--;
	}
}

void	ft_print_comb2(void)
{
	int		c;
	char	a[2];
	char	b[2];

	c = 0;
	a[0] = '0';
	a[1] = '0';
	b[0] = '0';
	b[1] = '1';
	while (a[0] != '9' || a[1] != '8')
	{
		if (a[0] < b[0] || (a[0] == b[0] && a[1] < b[1]))
		{
			write(1, a, 2);
			write(1, " ", 1);
			write(1, b, 2);
			write(1, ", ", 2);
		}
		comb_sec_up(b, &c);
		comb_fir_up(a, &c);
	}
	write(1, "98 99", 5);
}
